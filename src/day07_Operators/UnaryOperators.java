package day07_Operators;

public class UnaryOperators {

    public static void main(String[] args) {
        int num1=25;
        int num2=12;
        System.out.println(--num1);
        System.out.println(num2++);
        System.out.println(num2);

        int num3=5;
        ++num3;
        System.out.println(num3);

        int a=45;
        System.out.println(a--);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);





    }



}
